def get_invoice_type_config(invoice_type: str) -> Dict[str, Any]:
    """获取指定发票类型的配置"""
    return INVOICE_TYPES.get(invoice_type, INVOICE_TYPES["增值税普通发票"])

def get_all_field_patterns() -> Dict[str, str]:
    """获取所有字段模式"""
    all_patterns = {}
    for invoice_config in INVOICE_TYPES.values():
        all_patterns.update(invoice_config["fields"])
    return all_patterns

def get_invoice_types_list() -> List[str]:
    """获取所有发票类型列表"""
    return list(INVOICE_TYPES.keys())

def validate_invoice_data(extracted_fields: Dict[str, str]) -> Dict[str, Any]:
    """验证发票数据的完整性"""
    validation_result = {
        "is_valid": True,
        "missing_fields": [],
        "warnings": []
    }
    
    # 必需字段检查 - 只验证现在提取的字段
    # 项目名称和规格型号不是必需字段，所以不包含在验证中
    required_fields = ["开票日期", "价税合计小写"]
    for field in required_fields:
        if field not in extracted_fields or not extracted_fields[field]:
            validation_result["missing_fields"].append(field)
            validation_result["is_valid"] = False
    
    # 价税合计小写格式验证
    if "价税合计小写" in extracted_fields:
        amount = extracted_fields["价税合计小写"]
        try:
            # 移除逗号并转换为浮点数
            clean_amount = amount.replace(",", "")
            float(clean_amount)
        except ValueError:
            validation_result["warnings"].append("价税合计小写格式可能不正确")
    
    # 日期格式验证
    if "开票日期" in extracted_fields:
        date_str = extracted_fields["开票日期"]
        if not re.match(r"\d{4}[-年]\d{1,2}[-月]\d{1,2}[日]?", date_str):
            validation_result["warnings"].append("开票日期格式可能不正确")
    
    return validation_result